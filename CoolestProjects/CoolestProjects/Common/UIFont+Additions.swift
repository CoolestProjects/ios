import Foundation
import UIKit

extension UIFont {

    class func contentItemTitleFont() -> UIFont {
        return UIFont(name: "AvenirNext-Regular", size: 17.0)!
    }

    class func contentItemDescriptionFont() -> UIFont {
        return UIFont(name: "AvenirNext-Regular", size: 13.0)!
    }

}
